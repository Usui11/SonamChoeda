function opensignUp(){
    
    document.getElementsByClassName("signup-C")[0].style.display="block";
}

function closesignUp(){
   document.getElementsByClassName("signup-C")[0].style.display="none";
}

function opensignIn(){
     document.getElementsByClassName("signin-C")[0].style.display="block";
}

function closesignIn(){
     document.getElementsByClassName("signin-C")[0].style.display="none";
}


function modalSignUp(){
    document.getElementsByClassName("signin-C")[0].style.display="none";
    document.getElementsByClassName("signup-C")[0].style.display="block";
}

function opencreate(){
     document.getElementsByClassName("create")[0].style.display="block";
}

function closecreate(){
     document.getElementsByClassName("create")[0].style.display="none";
}

